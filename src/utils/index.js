export class Calculator {
    add(numb1, numb2) {
        const result = numb1 + numb2
        return result
    }

    minus(numb1, numb2) {
        const result = numb1 - numb2
        return result
    }

    times(numb1, numb2) {
        const result = numb1 * numb2
        return result
    }

    divided(numb1, numb2) {
        const result = numb1 / numb2
        return result
    }
}